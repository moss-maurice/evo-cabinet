DROP TABLE IF EXISTS `{table_prefix}orders`;
CREATE TABLE IF NOT EXISTS `{table_prefix}orders` (
    `id` int(11) NOT NULL AUTO_INCREMENT COMMENT 'Уникальный идентификатор',
    `tour_id` int(11) DEFAULT NULL COMMENT 'Идентификатор тура',
    `user_id` int(11) NULL COMMENT 'Идентификатор пользователя',
    `status_id` int(11) NOT NULL DEFAULT '1' COMMENT 'Идентификатор статуса',
    `price` float NOT NULL COMMENT 'Стоимость',
    `comment` text COMMENT 'Комментарий',
    `create_date` datetime DEFAULT CURRENT_TIMESTAMP COMMENT 'Дата создания',
    `update_date` datetime DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP COMMENT 'Дата обновления',
    UNIQUE KEY `UNIQUE` (`id`),
    KEY `status_id` (`status_id`),
    KEY `user_id` (`user_id`),
    KEY `tour_id` (`tour_id`),
    CONSTRAINT `{table_prefix}orders_ibfk_1` FOREIGN KEY (`status_id`) REFERENCES `{table_prefix}orders_statuses` (`id`) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT `{table_prefix}orders_ibfk_2` FOREIGN KEY (`user_id`) REFERENCES `{table_prefix}web_users` (`id`) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT `{table_prefix}orders_ibfk_3` FOREIGN KEY (`tour_id`) REFERENCES `{table_prefix}site_content` (`id`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE='InnoDB' DEFAULT CHARSET='utf8' COMMENT='Таблица заказов';

DROP TABLE IF EXISTS `{table_prefix}orders_payments_transactions`;
CREATE TABLE IF NOT EXISTS `{table_prefix}orders_payments_transactions` (
    `id` int(11) NOT NULL AUTO_INCREMENT COMMENT 'Уникальный идентификатор',
    `payment_id` varchar(32) DEFAULT NULL COMMENT 'Платежный идентификатор',
    `transaction_value` decimal(10,2) NOT NULL DEFAULT '0.00' COMMENT 'Значение оплаты',
    `transaction_type` int(5) NOT NULL DEFAULT '2' COMMENT 'Тип транзакции, по умолчанию: наличными в офис',
    `order_id` int(11) DEFAULT NULL COMMENT 'Номер заказа',
    `user_id` int(10) DEFAULT NULL COMMENT 'Данные плательщика',
    `editor` varchar(256) DEFAULT 'admin' COMMENT 'При корректировке цены от оператора',
    `comment` varchar(256) DEFAULT 'Оплата от заказчика' COMMENT 'Комментарий транзакции',
    `create_date` datetime DEFAULT CURRENT_TIMESTAMP COMMENT 'Дата создания',
    `update_date` datetime DEFAULT NULL ON UPDATE CURRENT_TIMESTAMP COMMENT 'Дата обновления (при добавлении соответствет дате создания)',
    `deleted` tinyint(4) NOT NULL DEFAULT '0' COMMENT 'Удаление транзакции',
    PRIMARY KEY (`id`),
    KEY `order_id` (`order_id`),
    KEY `user_id` (`user_id`),
    KEY `transaction_type` (`transaction_type`),
    CONSTRAINT `{table_prefix}orders_payments_transactions_ibfk_1` FOREIGN KEY (`user_id`) REFERENCES `{table_prefix}web_users` (`id`) ON UPDATE CASCADE,
    CONSTRAINT `{table_prefix}orders_payments_transactions_ibfk_2` FOREIGN KEY (`transaction_type`) REFERENCES `{table_prefix}transactions_types` (`id`) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT `{table_prefix}orders_payments_transactions_ibfk_3` FOREIGN KEY (`order_id`) REFERENCES `{table_prefix}orders` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE='InnoDB' DEFAULT CHARSET='utf8';

DROP TABLE IF EXISTS `{table_prefix}orders_properties`;
CREATE TABLE IF NOT EXISTS `{table_prefix}orders_properties` (
